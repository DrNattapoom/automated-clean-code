def add_numbers(x: int, y: int) -> int:
    """Add two numbers together.

    Args:a
      x (int): a number
      y (int): a number

    Returns:
      (int). The sum of the two numbers.
    """
    return x + y


def subtract_number(x: int, y: int) -> int:
    """Subtract two numbers.

    Args:
        x:
        y:

    Returns:
        x-y
    """
    return x - y
